let currentLocale = 'zh-Hans'

export function setLocale(locale?: string) {
    currentLocale = locale || currentLocale
}

export function getLocale() {
    return currentLocale
}

export function t(key: keyof typeof messages.en_US) {
    const locale = currentLocale.startsWith('zh') ? 'zh_Hans' : 'en_US'
    return messages[locale][key] ?? messages.en_US[key]
}
